"""Render `docs/codebase-roster.md` from `eval/roster/manifest.json`.

Reads the declarative roster + the per-codebase artifact paths listed there,
verifies the paths exist (so the table doesn't claim what we can't show), and
emits a single markdown table that's the entry point for the "multi-codebase
demonstration" claim (strategic-direction.md §4 P6).

CLI:

    python3 -m eval.roster.aggregate                  # writes docs/codebase-roster.md
    python3 -m eval.roster.aggregate --check          # exit 1 if any artifact path is missing
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import List


REPO_ROOT = Path(__file__).resolve().parents[2]
MANIFEST = REPO_ROOT / "eval" / "roster" / "manifest.json"
DOC_OUT = REPO_ROOT / "docs" / "codebase-roster.md"


def _artifact_exists(rel: str) -> bool:
    p = (REPO_ROOT / rel).resolve()
    return p.exists()


def _stage_a_cell(cb: dict) -> str:
    sa = cb.get("stage_a", {})
    s = sa.get("status", "?")
    if s == "done":
        red = sa.get("surface_reduction_pct")
        kept = sa.get("kept_units")
        total = sa.get("total_units")
        missed = sa.get("missed_bug_count", sa.get("missed_critical_path"))
        bits = [f"**{red}%**" if red is not None else "ok",
                f"({kept}/{total})" if kept is not None else "",
                f"missed={missed}" if missed is not None else ""]
        return " ".join(b for b in bits if b)
    if s == "shares":
        return f"shares: {sa.get('with', '?')}"
    if s == "not_setup":
        return "—"
    if s == "deferred":
        return "deferred"
    return s


def _stage_b_cell(cb: dict) -> str:
    sb = cb.get("stage_b", {})
    s = sb.get("status", "?")
    if s == "done":
        ch = sb.get("checked"); un = sb.get("unsafe"); sf = sb.get("safe")
        inc = sb.get("inconclusive"); fs = sb.get("false_safe")
        return f"checked={ch} unsafe={un} safe={sf} inconclusive={inc} false_safe={fs}"
    if s == "deferred":
        return "deferred"
    if s == "not_setup":
        return "—"
    return s


def _oracle_cell(cb: dict) -> str:
    orc = cb.get("oracle_runs", {})
    if not orc:
        return "—"
    lines = []
    for name, info in orc.items():
        st = info.get("status", "?")
        verdict = info.get("verdict") or info.get("note") or ""
        bits = [f"`{name}`={st}"]
        if verdict:
            bits.append(verdict)
        lines.append(" · ".join(bits))
    return "<br>".join(lines)


def _cex_cell(cb: dict) -> str:
    arts = cb.get("witness_artifacts", []) or []
    return f"{len(arts)}" if arts else "—"


def _missing_artifacts(cb: dict) -> List[str]:
    miss: List[str] = []
    for k in ("stage_a", "stage_b"):
        v = cb.get(k, {})
        if v.get("artifact") and not _artifact_exists(v["artifact"]):
            miss.append(f"{cb['id']}.{k}.artifact: {v['artifact']}")
    for run_name, run in cb.get("oracle_runs", {}).items():
        if isinstance(run, dict) and run.get("artifact") and not _artifact_exists(run["artifact"]):
            miss.append(f"{cb['id']}.oracle_runs.{run_name}.artifact: {run['artifact']}")
    for a in cb.get("witness_artifacts", []) or []:
        if not _artifact_exists(a):
            miss.append(f"{cb['id']}.cex_artifact: {a}")
    return miss


def render(manifest_path: Path = MANIFEST) -> str:
    m = json.loads(manifest_path.read_text())
    rows = []
    rows.append("# Codebase roster")
    rows.append("")
    rows.append("Generated by `python3 -m eval.roster.aggregate`. The source of truth is")
    rows.append("`eval/roster/manifest.json`; this doc is regenerated whenever a new")
    rows.append("codebase enters or leaves a state.")
    rows.append("")
    rows.append("Status semantics:")
    rows.append("")
    rows.append("- `done` — the analysis ran and an artifact is on disk")
    rows.append("- `deferred` — explicitly scoped out for a documented reason (see PROGRESS.md)")
    rows.append("- `not_setup` — codebase is registered but no run has happened yet")
    rows.append("- `shares` — analysis is shared with another roster entry (e.g. a kernel")
    rows.append("  config-restricted variant sharing the base slice)")
    rows.append("")
    rows.append("| ID | Kind | Stage A surface | Stage B (sound proof) | Oracle runs | Witness artifacts |")
    rows.append("|---|---|---|---|---|---|")
    for cb in m["codebases"]:
        rows.append("| `{id}` | {kind} | {sa} | {sb} | {orc} | {cex} |".format(
            id=cb["id"], kind=cb.get("kind", "?"),
            sa=_stage_a_cell(cb), sb=_stage_b_cell(cb),
            orc=_oracle_cell(cb), cex=_cex_cell(cb),
        ))
    rows.append("")
    rows.append("## Per-codebase notes")
    rows.append("")
    for cb in m["codebases"]:
        rows.append(f"### `{cb['id']}` — {cb.get('version', '?')}")
        rows.append("")
        if "scope" in cb:
            rows.append(f"- **Scope**: {cb['scope']}")
        if "loc" in cb and cb["loc"]:
            rows.append(f"- **LOC**: {cb['loc']:,}")
        if cb.get("total_tasks"):
            rows.append(f"- **Tasks**: {cb['total_tasks']:,}")
        if cb.get("sources"):
            rows.append(f"- **Sources**: {cb['sources']:,}")
        if cb.get("labeled_bad"):
            rows.append(f"- **Labeled `_bad`**: {cb['labeled_bad']:,}")
        arts = cb.get("witness_artifacts", []) or []
        if arts:
            rows.append(f"- **Witness artifacts** ({len(arts)}):")
            for a in arts:
                rows.append(f"  - `{a}`")
        rows.append("")
    return "\n".join(rows) + "\n"


def main(argv=None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--manifest", type=Path, default=MANIFEST)
    ap.add_argument("--out", type=Path, default=DOC_OUT)
    ap.add_argument("--check", action="store_true",
                    help="exit non-zero if any declared artifact path is missing")
    args = ap.parse_args(argv)

    if args.check:
        m = json.loads(args.manifest.read_text())
        missing: List[str] = []
        for cb in m["codebases"]:
            missing.extend(_missing_artifacts(cb))
        if missing:
            print("missing artifacts:")
            for line in missing:
                print(f"  - {line}")
            return 1
        print(json.dumps({"check": "ok", "codebases": len(m["codebases"])}))
        return 0

    rendered = render(args.manifest)
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(rendered)
    print(json.dumps({"out": str(args.out), "bytes": len(rendered)}))
    return 0


if __name__ == "__main__":
    sys.exit(main())
