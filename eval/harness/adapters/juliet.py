"""Juliet adapter — labeled C/C++ test cases (NIST SARD v1.3).

Used as the Phase 1.5 soundness gate (PLAN §2 acceptance): the Component (1)
pruning pipeline must keep every labeled `_bad`/`_bad_sink`/`_bad_source`
function (Stage A) AND the Stage B proof engine must never prove a `_bad`
function safe (CBMC).

Reads JSON results produced by:
  * eval/juliet/run_stage_a.py  -> eval/juliet/stage_a.json
  * eval/juliet/run_stage_b.py  -> eval/juliet/stage_b.json

Both files are regenerated by re-running those drivers; this adapter is a
reporter, mirroring the Phase 0.5 convention (see eval/harness/run_baseline.py).
"""
from __future__ import annotations

import json
from pathlib import Path

from ..metrics import MetricRow, make_row, REPO_ROOT

JULIET_DIR = REPO_ROOT / "eval" / "juliet"
STAGE_A_JSON = JULIET_DIR / "stage_a.json"
STAGE_B_JSON = JULIET_DIR / "stage_b.json"
EXTRACTED = JULIET_DIR / "extracted" / "C" / "testcases"


def _stage_a_row() -> MetricRow:
    if not STAGE_A_JSON.exists():
        return make_row(
            adapter="juliet", target="stage_a", status="not_setup",
            phase="1.5", success=False,
            notes="run eval/juliet/run_stage_a.py to populate stage_a.json",
        )
    d = json.loads(STAGE_A_JSON.read_text())
    s = d["stats"]
    missed = int(s["missed_bug_count"])
    reduction_pct = round(float(s["reduction"]) * 100.0, 2)
    return make_row(
        adapter="juliet", target="stage_a", status="success" if missed == 0 else "fail",
        phase="1.5",
        success=(missed == 0),
        verdict="soundness-pass" if missed == 0 else "soundness-fail",
        notes=(
            f"Stage A reachability over {s['c_files']} files / "
            f"{s['defined_functions']} fns; kept {s['keep_set']}; "
            f"{reduction_pct}% pruned; {s['labeled_bug_functions']} labeled "
            f"_bad funcs, {missed} missed"
        ),
        attack_surface_reduction_pct=reduction_pct,
        missed_bug_count=missed,
        evidence_paths=[str(STAGE_A_JSON.relative_to(REPO_ROOT))],
    )


def _stage_b_row() -> MetricRow:
    if not STAGE_B_JSON.exists():
        return make_row(
            adapter="juliet", target="stage_b", status="not_setup",
            phase="1.5", success=False,
            notes="run eval/juliet/run_stage_b.py to populate stage_b.json",
        )
    d = json.loads(STAGE_B_JSON.read_text())
    c = d["counts"]
    missed = int(d["missed_bug_count"])
    total = c.get("safe", 0) + c.get("unsafe", 0) + c.get("inconclusive", 0)
    return make_row(
        adapter="juliet", target="stage_b", status="success" if missed == 0 else "fail",
        phase="1.5",
        success=(missed == 0),
        verdict="soundness-pass" if missed == 0 else "soundness-fail",
        notes=(
            f"Stage B {d['engine']} on {total} _bad fns: "
            f"unsafe={c.get('unsafe',0)} safe={c.get('safe',0)} "
            f"inconclusive={c.get('inconclusive',0)} soundness_failures={missed}"
        ),
        missed_bug_count=missed,
        evidence_paths=[str(STAGE_B_JSON.relative_to(REPO_ROOT))],
    )


def baseline_rows() -> list[MetricRow]:
    if not EXTRACTED.exists():
        return [make_row(
            adapter="juliet", target="-", status="not_setup",
            success=False,
            notes="Juliet corpus not extracted; run Phase 1.5 setup",
        )]
    return [_stage_a_row(), _stage_b_row()]
